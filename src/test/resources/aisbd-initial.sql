ALTER PROCEDURE RENAME_DEFINITION (OLD_TYPE VARCHAR(80) CHARACTER SET WIN1251,
NEW_TYPE VARCHAR(80) CHARACTER SET WIN1251)
RETURNS (RESULT INTEGER)
AS 
declare variable OLD_CODE varchar(80);
declare variable NEW_CODE varchar(80);
declare variable TMP numeric(18,0);
begin

old_code = :old_type || ':1';
new_code = :new_type || ':1';

result = 0;

/* prevent renaming already renamed definitions (remove 'renamedFrom' prop val if you want ) */
select id from rtm_entity_property_value v
where v.entity_definition = :old_code
and v.property = 'renamedFrom'
into :tmp;
if (:tmp is not null) then
begin
  result = -1;
  suspend;
  exit;
end

/* create new with new type name */

INSERT INTO RTM_TYPE_DEFINITION (CODE, NAME, TYPE_NAME, SERIALIZER, VER, INLINE, VISUALIZER)
select            :new_code, name, :new_type, SERIALIZER, VER, INLINE, VISUALIZER
from rtm_type_definition where code = :old_code;

INSERT INTO RTM_ENTITY_DEFINITION (CODE, NAME_EXPRESSION, TO_STRING_EXPRESSION, QUICK_FILTER_EXPRESSION, DEFAULT_ORDER, PATH_EXPRESSION, DEFAULT_FOLDER, SUBENTITY_ONLY, RM_PROVIDER, TO_STRING_QUERY)
select :new_code,  NAME_EXPRESSION, TO_STRING_EXPRESSION, QUICK_FILTER_EXPRESSION, DEFAULT_ORDER, PATH_EXPRESSION, DEFAULT_FOLDER, SUBENTITY_ONLY, RM_PROVIDER, TO_STRING_QUERY
from RTM_ENTITY_DEFINITION where code = :old_code;

/* update all old */
update rtm_entity_filter t set t.entity_definition = :new_code where t.entity_definition = :old_code;
update rtm_entity_filter t set t.filter_value_type = :new_code where t.filter_value_type = :old_code;
update rtm_field_definition t set t.entity_definition = :new_code where t.entity_definition = :old_code;
update rtm_field_definition t set t.data_type = :new_code where t.data_type = :old_code;
update rtm_entity_form_panel t set t.entity_definition = :new_code where t.entity_definition = :old_code;
update rtm_entity_operation t set t.entity_definition = :new_code where t.entity_definition = :old_code;
update rtm_entity_property_value t set t.entity_definition = :new_code where t.entity_definition = :old_code;
update rtm_entity_validator t set t.entity_definition = :new_code where t.entity_definition = :old_code;


INSERT INTO RTM_ENTITY_PROPERTY_VALUE (ID, PROPERTY_VALUE, ENTITY_DEFINITION, PROPERTY)
select gen_id(hibernate_sequence, 1), :old_type, :new_code, 'renamedFrom'
from rdb$database;

/* delete old */
delete from rtm_entity_definition where code = :old_code;
delete from rtm_type_definition where code = :old_code;

result = 1;
suspend;
end ^
SET TERM ; ^
COMMIT WORK ;
SET AUTODDL ON;

